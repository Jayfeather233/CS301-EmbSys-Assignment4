extern uint8_t show_background;
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  HAL_Delay(50);
  switch (GPIO_Pin)
  {
  case KEY0_Pin:
    if (HAL_GPIO_ReadPin(KEY0_GPIO_Port, KEY0_Pin) != GPIO_PIN_RESET)
      break;
    
    LCD_Clear(WHITE); // 清屏
    TP_Adjust();      // 屏幕校准
    TP_Save_Adjdata();

    while (HAL_GPIO_ReadPin(KEY0_GPIO_Port, KEY0_Pin) == GPIO_PIN_RESET)
      ;
    break;
  case KEY1_Pin:
    if (HAL_GPIO_ReadPin(KEY1_GPIO_Port, KEY1_Pin) != GPIO_PIN_RESET)
      break;
    
    // TODO restart
    show_background ^= 1;

    while (HAL_GPIO_ReadPin(KEY1_GPIO_Port, KEY1_Pin) == GPIO_PIN_RESET)
      ;
    break;
  case KEY_WKUP_Pin:
    if (HAL_GPIO_ReadPin(KEY_WKUP_GPIO_Port, KEY_WKUP_Pin) != GPIO_PIN_SET)
      break;
    scence_id = scence_id^1; // deprecated
    // TODO: better switch

    MS_write_score(0);
    max_score = 0;

    while (HAL_GPIO_ReadPin(KEY_WKUP_GPIO_Port, KEY_WKUP_Pin) == GPIO_PIN_SET)
      ;
    break;
  default:
    break;
  }
}

#define DRAW_SKIP 5
int16_t draw_sk = 0;
int16_t logic_sk = 0;

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
  if (htim->Instance == TIM3){ // 30fps
    if(logic_sk == 0) {
      need_logic += 1;
      logic_sk = LOGIC_SKIP - 1;
    } else {
      -- logic_sk;
    }
    if(draw_sk == 0){
      need_draw += 1;
      draw_sk = DRAW_SKIP-1;
    } else {
      --draw_sk;
    }
  } else if (htim->Instance == TIM2){ // 30fps
    tp_dev.scan(0);
    update_movement(&tp_dev, obj_list_head.next);
  } else if (htim->Instance == TIM4){ // 1fps
    game_status_update();
  }
}
